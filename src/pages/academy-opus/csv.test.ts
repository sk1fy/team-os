import { describe, expect, it } from 'vitest';
import { toCsv } from './csv';

describe('toCsv', () => {
  it('оборачивает ячейки в кавычки и разделяет точкой с запятой', () => {
    expect(toCsv([['Иванов', 42]])).toBe('"Иванов";"42"');
  });

  it('экранирует кавычки внутри значения', () => {
    expect(toCsv([['Курс "Старт"']])).toBe('"Курс ""Старт"""');
  });

  it('не ломается на разделителе внутри значения', () => {
    expect(toCsv([['Продажи; и маркетинг']])).toBe('"Продажи; и маркетинг"');
  });

  it('пустые значения превращает в пустые ячейки', () => {
    expect(toCsv([['Иванов', undefined]])).toBe('"Иванов";""');
  });

  it('разделяет строки переводом CRLF', () => {
    expect(toCsv([['а'], ['б']])).toBe('"а"\r\n"б"');
  });
});
